// # 有效的括号

// ## 问题描述

// 给定一个只包含 `'('`、`')'`、`'{'`、`'}'`、`'['`、`']'` 的字符串 `s`，判断字符串是否有效。

// 有效字符串需满足：
// 1. 左括号必须用相同类型的右括号闭合
// 2. 左括号必须以正确的顺序闭合
// 3. 每个右括号都有一个对应的相同类型的左括号

// **例如：**
// ```
// 示例 1:
// 输入：s = "()"
// 输出：true

// 示例 2:
// 输入：s = "()[]{}"
// 输出：true
// 解释：所有括号都正确匹配

// 示例 3:
// 输入：s = "(]"
// 输出：false
// 解释：括号类型不匹配

// 示例 4:
// 输入：s = "([)]"
// 输出：false
// 解释：括号顺序不正确

// 示例 5:
// 输入：s = "{[]}"
// 输出：true
// 解释：嵌套括号正确匹配
// ```

function kuohao(s) {
    const stack = [];
    for (let i = 0; i < s.length; i++) {
        if (s[i] === '(') {
            stack.push(')');
        } else if(s[i] === '{') {
            stack.push('}');
        } else if (s[i] === '[') {
            stack.push(']');
        } else {
            if (!stack.length || stack.pop() !== s[i]) {
                return false;
            }
        }
    }
    return !stack.length
}